                <h2 style="font-weight: 600">Why the name Jicamahouse?</h2>
                <p>
                    Back when I lived in Tucson, the first house my partner and I had together was a tiny adobe house converted into a little bungalow in the middle of the city. We were broke, it was cheap, and probably violated many building codes since our hippie landlords renovated these old adobes houses themselves.
                </p>
                <p>
                    Each house they renovated they named after a fruit; ours was Jicama House. We didn’t have heat or A/C (a pretty rough ride in the 110 degree summers) but I loved this little house. I felt so inspired living in this little house, and I started really developing my photography skills here. I learned how to take and scan my own 35mm film photos, used the big, bright windows as lighting for portrait shoots, and documented almost everything we did for the two years we lived there.
                </p>
                <p>
                    I wanted to name my new endeavors after something that brought me joy and inspiration, so that’s how Jicamahouse Photography was born. 
                </p>
